def climbingLeaderboard(ranked, player):
    res  = []
    # ranked = list(set(ranked))
    for i in player:
        j = 0
        k = 0
        last = -1
        while k < len(ranked) and ranked[k] > i:
            if not last == ranked[k]:
                j+=1
            last = ranked[k]
            k += 1
        res.append(j+1)
    return res

